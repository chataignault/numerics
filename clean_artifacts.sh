#!/bin/bash

# ============================================================================
# CLEAN COMPILATION ARTIFACTS FROMT THE REPO
# ============================================================================


# ----------------------------------------------------------------------------
# RUST
# ----------------------------------------------------------------------------
echo "========================================"
echo "===Clear Rust crates with cargo clean==="
echo "========================================"
find . -type f -path "./*/Cargo.lock" |\
	xargs -I {} sh -c \
	'echo "Cleaning $1 ..." && cd $(dirname "$1") && cargo clean && cd -' _ {}


